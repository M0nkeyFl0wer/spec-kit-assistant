deploy_to_repo() {
    local repo_url="$1"
    local repo_name="$2"
    local deploy_dir="$WORK_DIR/$repo_name"

    echo "ğŸš€ Deploying to $repo_name repository..."

    cd "$deploy_dir"

    # Initialize git if needed
    if [ ! -d ".git" ]; then
        git init
        git remote add origin "$repo_url"
    fi

    # Configure git safely (without personal info)
    git config user.name "Spec Kit Assistant"
    git config user.email "spec-kit@claude-code.ai"

    # Add all safe files
    git add .

    # Check if there are changes
    if git diff --staged --quiet; then
        echo "âœ… No changes to deploy"
        return 0
    fi

    # Create commit
    local commit_msg="ğŸ¤– Spec Kit Assistant update: $(date '+%Y-%m-%d %H:%M:%S')

ğŸ”’ Security: All secrets pruned
ğŸ§¹ Clean: Only safe files included
ğŸŒ± Generated by Ultimate Toolkit

